function J = Six_Jac(y)
    y = reshape(y,6,1);
    x1 = y(1);
    x2 = y(2);
    u1 = y(3);
    u2 = y(4);
    u3 = y(5);
    u4 = y(6);
    J = [0, 1, 0, 0, 0, 0; ...
        -u2, -u2, 0, -(x1+x2), 0, 0; ...
        (x2-x1*u2-x2*u2)*u2-u2*(x1+x2)*u2, (x2-x1*u2-x2*u2)*u2+(x1+x2)*(1-u2)*u2, 0, (x1+x2)*(x2-x1*u2-x2*u2)+(x1+x2)*(-x1-x2)*u2, 0, 0; ...
         -(x2-x1*u2-x2*u2)*u2^3+u2*(x1+x2)* u2^3, -(x2-x1*u2-x2*u2)*u2^3-(x1+x2)*(1-u2)* u2^3, 0,  -(x1+x2)*(x2-x1*u2-x2*u2)*3*u2^2-(x1+x2)*(-x1-x2)*u2^3,  0, 0; ...
        x2*u4,    x1*u4,      0, 0, 0,  x1*x2;...
        -x2*u4^3, -x1*u4^3, 0, 0, 0, -x1*x2*3*u4^2;];
   J = J([1:2 4], :);
   J = J(:, [1:2 4]);